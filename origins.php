	<h2>Ragtime et blues (1890-1910)</h2>

	<p>Au début du xxe siècle, le blues se développe dans le Delta du Mississippi et est largement diffusé à partir de 1920 avec entre autres le premier enregistrement de Mamie Smith. Parallèlement, le ragtime apparaît, style de piano incarné par Scott Joplin, musique syncopée influencée par la musique classique occidentale. Dans les années 1920, le stride se développe à Harlem. Héritier du ragtime, le stride introduit l'utilisation d'une pulsation ternaire, et la virtuosité des musiciens augmente, comme chez James P. Johnson. Le boogie-woogie se développe à la même époque à Chicago.</p>
