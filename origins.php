	<h2>Du swing au bebop</h2>

	<p>Considéré comme l'âge d'or du jazz, apparu vers les années 1930, le swing (ou middle jazz) se démarque du jazz Nouvelle-Orléans par un orchestre de plus grande taille sur le modèle des trois sections de trompettes, trombones et anches qui privilégie les solistes prenant des chorus intégrés dans des arrangements écrits au détriment de l'improvisation collective. C'est l'ère des big bands de Duke Ellington, Count Basie, Glenn Miller, Benny Goodman, avec un répertoire marqué par les compositions de George Gershwin, Cole Porter, Richard Rodgers etc. et les chansons de variété de Tin Pan Alley, qui forment l'ossature des standards de jazz. Les grands solistes de cette époque sont Coleman Hawkins, Roy Eldridge, Benny Carter, Johnny Hodges, Ben Webster, Art Tatum, et Lester Young. </br>

Au début des années 1940 naît le bebop. Tempos ultras rapides, petites formations, virtuosité époustouflante, innovations harmoniques et rythmiques, la rupture est brutale et emmenée par Charlie Parker, Dizzy Gillespie, Kenny Clarke, Thelonious Monk. L'intellectualisation du jazz par le bebop ne sera pas toujours bien perçue par le public et certains critiques, notamment Hugues Panassié en France sera particulièrement virulent contre cette nouvelle forme de jazz.

</p>
