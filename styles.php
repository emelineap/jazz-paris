	<h2>BE BOP</h2>

	<p>Les premiers enregistrements datent de 1945. Ces groupes de jazz sont souvent composés d'une poignée de musiciens qui recherchaient après leurs obligations contractuelles dans de grands orchestres à s'affranchir de la discipline des big bands. Ces formations plus réduites laissent plus de liberté dans l'interprétation et plus d'opportunités de jouer des solos. Le be-bop se singularise desautres styles de jazz par un tempo souvent très rapide, des phrasés pêchus et des grilles harmoniques très fournies, les accords changent toutes les mesures voire très souvent plusieurs fois par mesure. Ce style demande donc une grande maîtrise technique de son instrument ainsi qu'une bonne oreille et une connaissance assez approfondie de la théorie musicale.<br />
	Musiciens : Charlie Parker - Dizzy Gillespie - Thelonius Monk </p>
