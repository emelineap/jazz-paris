	<h2>Ragtime</h2>

	<p>Le ragtime est un genre musical d'origine américaine extrêmement populaire dans les années 1900-1918. On trouve dans le Cakewalk et les musiques de salon une forme primitive de ragtime, mais on situe généralement l'émergence du ragtime classique en 1897, année où furent publiées plusieurs compositions importantes de ragtime (communément appelées rags). Du point de vue musical, le ragtime se caractérise principalement par l'introduction de syncope dans la mélodie : les notes sont parfois jouées entre les temps principaux du rythme, créant un effet de décalage.<br />
	Musiciens : Scott Joplin - Joseph Lamb - James Scott.</p>
