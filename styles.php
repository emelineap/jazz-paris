	<h2>FUSION</h2>

	<p>Le jazz-rock, appelé parfois jazz fusion, est un style musical né à la fin des années 1960. Ce style est lancé par Miles Davis, lorsque celui-ci prend ses distances avec le be-bop pour se tourner vers la musique amplifiée, mariant les influences du jazz, du rock, du rhythm and blues, du soul music, du funk et parfois de la musique classique. Ses albums « Bitches Brew » et « In a Silent Way » (1969) sont considérés comme l'acte de naissance de ce style. Un autre album incontournable du style est « The Inner Mounting Flame» (1971) du Mahavishnu Orchestra de John McLaughlin.<br />
	Musiciens : Miles Davis - John McLaughlin - Weather Report - Uzeb. </p>
		</div>

