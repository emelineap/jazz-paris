	<h2>LATIN</h2>

	<p>Le jazz latin est le mariage des rythmes issus de l'Afrique et de l'Amérique latine et du jazz américain. Les deux catégories principales sont l'afro-cubain et le brésilien. L'afro-cubain inclut salsa, merengue, songo, son, mambo, bolero, charanga et cha cha cha. Le brésilien inclut le bossa-nova et la samba. L'afro-cubain prends ses racines à la fin des années quarante quand Dizzy Gillespie et Stan Kenton combinent les sections rhythmiques et structures de l'afro-cubain, tels que Machito et ses afro-cubains avec des instruments de jazz et l'improvisation. Poncho Sanchez lance Conga Blue un classique du genre.<br />
	Musiciens : Poncho Sanchez - Machito - Antonio Carlos Jobim.</p>
