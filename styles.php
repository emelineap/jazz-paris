	<h2>New Orleans</h2>

	<p>Le jazz Nouvelle-Orléans (New Orléans Jazz) est un courant musical du jazz qui s'est principalement développé dans les années 1910 et 1920 à la Nouvelle-Orléans — c'est là qu'est né le jazz dans les années 1910 avant d'émigrer vers Chicago — avant de s'éclipser face au middle jazz ou swing vers la fin des années 1920. L'une des caractéristiques de ce courant est le style « tailgate », consistant pour le trombone à accompagner le jeu de la trompette par un contrepoint rythmique, ponctué d'effets de glissandi. Le nom « tailgate » vient du fait que les orchestres défilaient sur un chariot traîné par des chevaux.<br />
	Musiciens : Louis Armstrong - Jelly Roll Morton - King Oliver. </p>
