	<h2>FREE</h2>

	<p>Le free jazz est un style de musique qui se développe dans les années 1950-1960. John Coltrane est un des musiciens qui, dans ses dernières œuvres, réalise quelques-uns des meilleurs morceaux de free jazz. Habituellement, cette musique est jouée par de petits groupes de musiciens. Dans l'esprit populaire, le free jazz est lourd, agressif et dissonant. Beaucoup de critiques pensent que l'abandon des éléments familiers du jazz est dû à un manque de technique de la part des musiciens. Ce point de vue est devenu marginal et cette musique a pu se construire une forte tradition. Le free jazz utilise les bases du jazz mais avec une composition moins structurée que les styles précédents. L'improvisation, par exemple, y tient une grande place, le free jazz étant d'ailleurs un des principaux « inspirateurs » du genre improvisation libre.<br />
	Musiciens : John Coltrane - Ornette Coleman - Cecil Taylor - John Zorn </p>
