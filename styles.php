	<h2>Swing</h2>

	<p>Le middle jazz ou swing (jazz du « milieu ») est un courant musical du jazz qui prend son essor du milieu des années 20. Il est surtout caractérisé par le développement des grands orchestres et big bands et du swing. Par extension, ce courant est également appelé «ère des big bands», «époque du swing» ou tout simplement «swing». Un élément du swing est le développement des sections de cuivres, et particulièrement des saxophones. Chaque orchestre acquiert une identité et une couleur qui lui est propre, surtout grâce au travail des arrangeurs et des chefs d’orchestre qui adaptent les compositions à la personnalité de l’orchestre, voire de chaque musicien.<br /> 
	Musiciens : Duke Ellington - Count Basie - Benny Goodman.</p>
