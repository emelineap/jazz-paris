	<h2>Manouche</h2>

	<p>Swing inspiré du jeu du guitariste manouche Django Reinhardt (1910-1953). Ce style de jazz se fonde sur le répertoire enregistré par Django Reinhardt c.-à-d. ses propres compositions telles Minor Swing et Nuages et des standards de jazz des années 20 et 30 tels qu' All of Me, After You've Gone ou I Can't Give You Anything But Love. Ce style s'illustre par l'absence de batterie et de cuivres.  Il s'identifie par une rythmique assumée par une ou plusieurs guitares qui accentuent les 2e et 4e temps de la mesure de 4/4. La contrebasse jouera dans un style de swing traditionnel et accentuera les 1er et 3e temps. Le premier soliste, habituellement guitariste se verra donner la réplique soit par un violoniste, un clarinettiste ou un accordéoniste.<br />
	Musiciens : Django Reinhart - Bireli Lagrene.</p>
