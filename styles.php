	<h2>HARD BOP</h2>

	<p>Cette musique début en 1955-1960 et est un retour aux sources de la musique, à l'Afrique et, en même temps, une réaction agressive (musicalement parlant) au cool jazz (d'où le terme « hard ») surtout dominé par les blancs. Même si la plupart des acteurs de ce courant on fait leur apprentissage dans le style bebop (d'où le terme « bop »), ce genre musical incorpore les influences du rhythm and blues, du blues et du gospel. Les morceaux de hard bop ont généralement un tempo plus lent que le bebop, et si le hard bop en reprend les innovations harmoniques, la part du rythme y est nettement plus marquée, sans doute en raison de la contribution majeure des batteurs Max Roach et Art Blakey.<br />
	Musiciens : Art Blakey - Max Roach - Sonny Rollins</p>
